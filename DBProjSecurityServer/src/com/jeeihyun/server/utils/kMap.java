package com.jeeihyun.server.utils;

public class kMap {
    String label;
    String value;
    
    public kMap(){
        label = "";
        value = "";
    }

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}    
    

    
    
    
}
